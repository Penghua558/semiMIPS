`define ADD 4'h0 // add opcode
`define SUB 4'h1 // subtract opcode
`define NEG 4'h2 // 2's complement opcode
`define ADDU 4'h3 // add unsigned
`define SUBU 4'h4 // subtract unsigned
`define AND 4'h5 // and opcode
`define OR 4'h6 // or opcode
`define XOR 4'h7 // xor opcode
`define NOT 4'h8 // 1's complement opcode
`define ASL 4'h9 // arithmetic shift left opcode
`define ASR 4'hA // arithmetic shift right opcode
`define LSL 4'hB // logical shift left opcode
`define LSR 4'hC // logical shift right opcode
